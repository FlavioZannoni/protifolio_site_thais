// Desde Freud até os psicólogos contemporâneos, a disciplina evoluiu significativamente, incorporando diversas abordagens teóricas, como o behaviorismo, a psicologia cognitiva, a psicologia humanista e a psicologia social. Cada abordagem oferece uma perspectiva única sobre a mente humana e seu funcionamento.

